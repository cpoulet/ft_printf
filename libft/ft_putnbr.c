#include "libft.h"

void	ft_putnbr(int nb)
{
	long n;

	n = nb;
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n > 9 || n < -9)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
