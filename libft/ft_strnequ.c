#include "libft.h"

int	ft_strnequ(const char *s1, const char *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	if (n == 0)
		return (1);
	while (*s1 == *s2++ && n-- > 0)
		if (*s1++ == '\0' || n == 0)
			return (1);
	return (0);
}
