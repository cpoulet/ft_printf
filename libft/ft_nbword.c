#include "libft.h"

int	ft_nbword(const char *s, char c)
{
	int nb;

	nb = 0;
	while (*s && *s == c)
		s++;
	while (*s)
	{
		while (*s && *s != c)
			s++;
		nb++;
		while (*s && *s == c)
			s++;
	}
	return (nb);
}
